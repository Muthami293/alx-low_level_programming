#include "3-calc.h"

/**
  * op_add - function to add to int
  * @a: first int
  * @b: second int
  * Return: int as result
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract
 * @a: first int
 * @b: second int
 * Return: int as result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - function to multiply two int
  * @a: first int
  * @b: second int
  * Return: int as result
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - function to divide
  * @a: first int
  * @b: second int
  * Return: int as result
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function for modulos
 * @a: first int
 * @b: second int
 * Return: int as result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
