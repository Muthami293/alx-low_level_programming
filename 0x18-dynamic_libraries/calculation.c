#include "calc.h"

/**
  * add - function to add two numbers
  * @a: first operand
  * @b: second operand
  * Return: sum
  */

int add(int a, int b)
{
	return (a + b);
}


/**
  * sub - function to subracts two numbers
  * @a: first operand
  * @b: second operand
  * Return: remainder
  */

int sub(int a, int b)
{
	return (a - b);
}

/**
  * mul - function to multiply two numbers
  * @a: first operand
  * @b: second operand
  * Return: product
  */

int mul(int a, int b)
{
	return (a * b);
}

/**
  * div - function to divide two numbers
  * @a: first operand
  * @b: second operand
  * Return: quotient
  */

int div(int a, int b)
{
	return (a / b);
}

/**
  * mod - function find mod of two nums
  * @a: first operand
  * @b: second operand
  * Return: remainder
  */

int mod(int a, int b)
{
	return (a % b);
}
